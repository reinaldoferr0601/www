<p>This comprehensive program is comprised of three different phases.</p>
<p id="sps_pps"><em>Phase I - Initiation:</em> The trader in training takes iFundTraders’ intense Training Course that covers all the Oliver Velez trading strategies currently used by our professional traders to make their livelihood via the markets. </p>
<p id="sps_pps"><em>Phase II - Neophyte:</em> The trader is given a simulated trading account with which to prefect the tactics, strategies and execution skills taught in the intensive Master Trader Program. During this phase the trader is mentored, nurtured and guided each day via our live online moderated trading room and has the single goal of advancing to level 4 of our Trader Advancement Table. As an incentive, training bonuses are awarded to the trader who performs well during this phase. The simulated account must have an overall net positive balance in order to qualify for graduation.</p>
<p id="sps_pps"><em>Phase III - Professional:</em> Upon graduating from Phase II, the trader is given the option of becoming a funded member of our professional trading firm. Each trader who opts for this offer will be given access to the firm’s account and will receive 100% of all profits until the entire educational fee paid to iFundTraders has been recouped. Once the trader has received full reimbursement for his training fees, the trader and the firm will share in the profits going forward, starting with an 80/20 split in favor of the trader. </p>
 <p id="sps_pps"><em>Phase IV - Master Trader:</em> The trader becomes a colony leader of other traders and receives a percentage of their trading profits. As a Master Trader, a colony leader may earn income not only for their own trading efforts, but also on the efforts of other traders under their direct tutelage. The bigger and more profitable the colony is made, the larger will his or her income become. This is the ultimate step in the process toward becoming a professional trader. Colony leadership helps 
the trader leverage his hard earned skill and catapults the trader into the business side of the trading industry.</p>
 
<p><strong>Risk Capital –</strong> No risk capital is required. Upon full graduation of the iFundTraders Course, you begin trading with $75,000 in buying power and can move to as much as $2,000,000 in buying power, after certain performance goals have been met.</p>
<p><strong>Phase III Trading Rate –</strong> Once a trader becomes a professional with our preferred trading firm, their trading rate is the same as the firm’s institutional trading rate. Currently, there is no other firm we are aware of in the U.S. who trades at such a low trading rate.</p>
<p><strong>Live Funded Trading Account –</strong> iFundTraders students who fully graduate can ultimately get unlimited access to our preferred trading firm’s capital, but must earn their way to higher levels of exposure. Performance goals are set for each professional trader at the start of their career.</p>

<p><strong>iFundTraders Phase III Payout Schedule –</strong> iFundTraders who enter into Phase III to become professional traders start off by getting 100% of all profits until their entire education fee has been recouped. After full reimbursement through profits, the trader and the firm share profits with 80% going to the trader at the lower levels and as much as 95% of the profits going to the trader at the highest levels. </p>
<p><strong>Retakes –</strong> iFundTraders Master Trader students get free retakes of all iFundTraders training events free of charge.</p>
<p><strong>Tuition Reimbursement Program – </strong>Once a student enters Phase III and becomes a professional trader for our preferred institutional trading firm, 100% of all profits are paid out to the trader until the entire educational fee has been reimbursed. Profit sharing with the trading firm begins after full reimbursement, starting with the trader receiving 80% of all profits to as much as 95% at the highest levels. </p>

