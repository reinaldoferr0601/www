            <h2><strong>Frequently Asked Questions</strong></h2>
            <h3>We’ve got the answers</h3>
          </div>
          <img src="common/images/top_img_faq.png" width="395" alt="" />
<p>
      <b> Is iFundTraders, LLC a brokerage firm?</b><br />
No. iFundTraders, LLC is a New York City based educational trading firm which trains and prepares individuals to join the ranks of professionals by becoming a member of an elite team of traders who are fully funded and share in profitability with the firm. Upon full completion of our training program, each trader is offered the option to become a member of and trade the account of our preferred trading firm.   </p>
<p>
<b>Is iFundTraders hiring me? </b><br />
No. You are not being hired in the traditional sense with benefits and a salary. If you complete our 7-day education package, you will be offered an opportunity to trade for our preferred trading firm as professional trader. You will be allocated a live funded account to trade on that firm's behalf and have the opportunity to earn a living from trading the markets. You will be compensated based on your performance as a professional trader with the firm.   </p>
<p>
<b>What will my responsibilities be as a professional trader, once I’m fully graduated from the iFundTraders Master Training Program? </b><br />
Your only duty will be to trade. iFundTraders students will be taught several proven methods that have produced consistent gains for many of our existing traders. Over the course of a few months, the trader will ultimately settle on a style that best suits their profile and personality. Styles of trading can never be generic. They must match the trader's psychological make-up. This is why one trader can make a fortune with one method, while another trader can lose a fortune with the very same one. We will help you find what style of trading is best suited for you.  </p>

<p><b>Do I need to deposit $5,000 risk capital or any other amount once I’m ready for trading?</b><br />
No! A risk deposit is not necessary for those who graduate from our training program and decide to become professionals. Our graduates begin their professional trading careers trading firm capital. They do not risk their own money. In fact, the firm does not profit unless you profit. In a very real sense, the firm becomes partners only in your success as a trader. If you lose and call it quits, all the losses are the firms. It's only when you begin to make money that the firm benefits. This gives us and our preferred trading firm every incentive in the world to do our best to turn you into a profitable trader, as quickly as possible. </p>

<p><b>Do I receive a salary if iFundTraders hires me as a trader?</b><br />
Each iFundTraders student has the ability to become hired as professional traders with our preferred trading firm, at which they are paid on performance only. Compensation is based on each individual trader's net profit and loss. We provide a carefully structured Trader Advancement Program that will increase the capital that you can trade as you demonstrate consistent profitability. There is no cap on the levels that can be achieved, so your potential revenue is virtually unlimited.  </p>

<p><b>I like everything I have read, is training and trading remotely for iFundTraders possible? </b><br />
Yes! Years ago, the answer to this question would have been a resounding "no." Today, due to incredible technological advancements in the professional trading field, we can monitor, assist, and teach a trader remotely, just as effectively as one on-site. Many traders do better from the comfort and solitude of their own homes. What's more, our remote trainees and traders experience all that happens in a main trading floor through our private web-based (virtual) trading room.  </p>

<p><b>How do I get started as a student and trader? </b><br />
To become an iFundTraders graduate, you must satisfactorily complete the following two phases of training: </p>

<p><strong><i>Phase I:</i></strong> The Master Trader Program – This is seven full days of intense training. This is the program that has produced the largest number of trading successes for iFundTraders. <br />
<strong><i>Phase II:</i></strong> Simulated Trading Mode – This is the phase in which each trader is given a simulated trading account, complete with real, live market data with which to perfect the trading tactics and sophisticated strategies taught in the Master Trader Program. Upon successfully advancing to level 4 of our Trader Advancement Table, the trader is then eligible to become a true professional trader with a funded account at our preferred trading firm. This is what is called Phase III: Professional Trading. 100% of all profits go to the trader until all education fees paid to iFundTraders has been recouped. This in effect makes the entire program free for those who progress </p>
  <p>
<b>When can I withdraw a part or all of my profits once I go pro in Phase III? </b><br />
Monthly trading profit checks are dispersed each month following the one in which they were generated. As an example, profits earned during the month of January would be paid no later than the 20th of February.
</p>
<p>
<b>Can I request additional buying power as a professional trader in Phase III?</b><br />
Yes. However, this increase will always be granted based on your trading performance. In our structured program, the professional trader in Phase III earns greater and greater exposure to more capital. As most traders know, the bigger the buying power, the bigger the potential profits. This extra potential is earned via "consistency" more than anything else. In the professional trading world, consistency is king, consistency married with low draw-downs and/or small account swings. Some traders have developed a consistency that fosters very few losing days during each month, with account swings that are nearly nonexistent.  </p>

<p><b>I love the program being offered at iFundTraders, but I can't trade full-time at the moment. Can I become a part-time iFundTraders student?</b><br />

Yes. Part-time traders often grow into some of our best traders. First of all, their growth and development as professional traders happen in smaller, more digestible periods. Secondly, the lessons learned are steady and the part-time nature of their trading reduces the risk of being overloaded and overwhelmed with too much at one time. Some of the best full-time traders today began as part-time traders first.  </p>

<p><b>Is there an additional fee for retaking the training provided by iFundTraders?</b><br />
No. As iFundTraders students, you will be allowed to retake all the education provided free of charge. There is never an additional fee for education, which makes our program very powerful and forever rewarding. In fact, iFundTraders encourages traders to take the full training program no less than three times. Some have taken it as many as five times. Once you fully graduate and are inducted into our trading organization as a professional trader, you are a part of our family of traders. Your education and nurturing will never end as iFundTraders will continue to mentor you for as long it takes to make you a more successful trader.</p>

<p><b>What is your refund/cancellation policy for your training programs?</b><br />
The point of sale is completed and delivered once payment is made, as Program Services are provided immediately within the members “Online Account Control Center”, which includes access to the copyrighted and proprietary 300 plus page iFT Training Manual, which contains proven strategies, which could provide traders to make full time income, when utilizing the strategies provided, along with consistent self discipline. Access to the iFT Online Trading Room, where we discuss, train, and educate traders daily, on how to consistently pull profits from the markets, in a real live market environment. Access to the paid iFT Online Training Room, where we discuss, train, and educate traders daily, on how to consistently pull profits from the markets, in an online classroom type of environment. Access to the iFT Online Support Room, where we provide technical support to all traders, using our proprietary platforms. Access to Platform Download, and training videos for the platforms, which was developed by our top instructors, specifically for our traders. Members receive immediate access to these proprietary offerings upon  payment; rendering refund or cancelation requests invalid once Program Services have been established. With that said, each iFundTraders student (of either Equity or Forex Master Trader Programs) can earn back all educational fees paid via trading profits. Each Phase III trader gets 100% of all profits until all educational fees have been recouped. Only after full reimbursement does the trading firm begin to share in the profitability of the trader. </p>


<p><b>Can I provide payment via credit card with iFundTraders?</b><br />
Yes,   you can pay using a credit card, but all credit card payments are   non-refundable, and you must reply to an email agreement stating that   you understand this policy before we process your initial payment.</p>



