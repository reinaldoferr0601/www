          <img src="common/images/top_img_faq.png" width="395" alt="" />
<p>
      <b>A iFundTraders, LLC uma corretora?</b><br />
Não. A iFundTraders, LLC é uma empresa de base educacional de trading de New York City, que treina e prepara os indivíduos para se juntarem ao patamar de profissionais, tornando-se um membro de uma equipe de traders de elite que são totalmente financiados e compartilham parte da sua lucratividade com a empresa. Após a conclusão total do nosso programa de treinamento, cada trader é oferecido a opção de se tornar um membro e operar a conta da nossa empresa de trading preferida.   </p>
<p>
<b>A iFundTraders está me contratando? </b><br />
Não. Você não está sendo contratado no sentido tradicional, com benefícios e um salário. Se você completar o nosso pacote de educação de 7 dias, você será oferecido uma oportunidade de operar para a nossa empresa de trading preferida como trader profissional. Será atribuído uma conta real financiada para operar em nome dessa empresa e ter a oportunidade de ganhar a vida com trading dos mercados. Você será compensado com base no seu desempenho como um trader profissional com a empresa.   </p>
<p>
<b>Quais serão as minhas responsabilidades como um trader profissional, uma vez que eu estou totalmente formado na Master Training Program da iFundTraders? </b><br />
Seu único dever será o de operar. Alunos da iFundTraders serão ensinados vários métodos comprovados que produzirem ganhos consistentes para muitos de nossos traders existentes. Ao longo de alguns meses, o trader acabará se confortando em um estilo que melhor se adapte o seu perfil e personalidade. Estilos de trading nunca podem ser genéricos. Eles devem corresponder a composição psicológica do profissional. É por isso que um traders podem fazer uma fortuna com um método, enquanto outro traders podem perder uma fortuna com o mesmo. Nós vamos ajudá-lo a encontrar o estilo de trading que é o mais adequado para você.  </p>

<p><b>Preciso depositar $ 5.000 de capital de risco ou qualquer outro montante, uma vez que estou pronto para operar?</b><br />
Não! Um depósito de risco não é necessário para aqueles que se formam a partir de nosso programa de treinamento e decidem se tornar profissionais. Os nossos alunos começam suas carreiras profissionais de trading operando o capital da empresa. Eles não arriscam o próprio dinheiro. Na verdade, a empresa não lucra a menos que você lucre. Em um sentido muito real, a empresa torna-se parceira apenas em seu sucesso como um trader. Se você perder e desistir, todas as perdas são da empresa. É só quando você começa a fazer dinheiro é que a empresa se beneficia. Isso dá a nós e a nossa empresa de trading preferida todo o incentivo do mundo para fazer o nosso melhor para transformá-lo em um trader lucrativo, o mais rápido possível. </p>

<p><b>Recebo um salário se a iFundTraders me contrata como um trader?</b><br />
Cada aluno da iFundTraders tem a capacidade de ser contratado como um trader profissional com a nossa empresa de trading preferida, pelo qual são pagos apenas pelo desempenho. A remuneração é baseada no lucro líquido e perda de cada trader individual. Nós fornecemos um Programa de Avanço do Trader de estruturada cuidadosa que vai aumentar o capital que você pode operar ao que você demonstrar lucratividade consistente. Não há limite para os níveis que podem ser alcançados, então sua receita potencial é praticamente ilimitada.  </p>

<p><b>Eu gosto de tudo que já li, é possível me formar e operar remotamente para a iFundTraders? </b><br />
Sim! Anos atrás, a resposta a esta questão teria sido um sonoro &quot;não&quot;. Hoje, devido aos incríveis avanços tecnológicos na área de trading profissional, podemos acompanhar, auxiliar e ensinar um trader remotamente, tão eficazmente quanto presencialmente. Muitos traders desempenham melhor do conforto e da quietude de suas próprias casas. Além do mais, os nossos trainees e traders remotos experimentam tudo o que acontece em um pregão principal através da nossa sala de trading privada com base na web (virtual).</p>

<p><b>Como faço para começar como um estudante e trader? </b><br />
Para se tornar um graduado da iFundTraders, você tem que completar satisfatoriamente as duas seguintes fases do treinamento: </p>

<p><strong><i>Fase I:</i></strong> O Programa Master Trader - Este é um treinamento intenso de sete dias inteiros. Este é o programa que produziu o maior número de traders de sucesso para a iFundTraders. <br />
<strong><i>Fase II:</i></strong> Modo de Treinamento Simulado - Esta é a fase em que cada trader é dado uma conta de trading simulada, com dados de mercado em tempo real com o qual deve aperfeiçoar as táticas e estratégias sofisticadas de trading, ensinadas no Programa Master Trader. Após avançar com sucesso para o nível 4 da nossa Tabela de Avanço de Trader, o trader é então elegível para se tornar um verdadeiro trader profissional com uma conta financiados em nossa empresa de trading preferida. Isto é chamado de Fase III: Trading Profissional. 100% de todos os lucros vão para o trader até que todo o custo educacional pago a iFundTraders tenha sido recuperado. Este efeito faz com que todo o programa seja gratuito para aqueles que progridem.</p>
  <p>
<b>Quando posso retirar uma parte ou todos os meus lucros, uma vez que eu fui para a Fase III? </b><br />
Cheques de lucro de trading mensal são enviados cada mês seguinte daquele em que foram gerados. Como exemplo, os lucros ganhos durante o mês de janeiro serão pagos o mais tardar até o dia 20 de fevereiro.
</p>
<p>
<b>Posso solicitar poder de compra adicional, como um trader profissional na Fase III?</b><br />
Sim. No entanto, este aumento será sempre concedida com base no seu desempenho de trading. No nosso programa estruturado, o trader profissional na Fase III ganha cada vez maior exposição a mais capital. Como a maioria dos traders sabem, quanto maior o poder de compra, o maior os potenciais de lucros. Este potencial adicional é ganho através de &quot;consistência&quot; mais do que qualquer outra coisa. No mundo de trading profissional, a consistência é rei, a consistência casou com baixas quedas e / ou oscilações de pequenas da conta. Alguns tarders têm desenvolvido uma consistência que promove muitos poucos dias de perda durante cada mês, com oscilações da conta que são quase inexistentes. </p>

<p><b>Eu amei o programa que está sendo oferecido pela iFundTraders, mas não posso operar em período integral no momento. Posso me tornar um estudante da iFundTraders de período parcial?</b><br />

Sim. Traders de período parcial, muitas vezes viram alguns dos nossos melhores traders. Primeiro de tudo, o seu crescimento e desenvolvimento como trader profissional acontecem em períodos menores, mais digeríveis. Em segundo lugar, as lições aprendidas são estáveis e o trading de período parcial reduz o risco de ser sobrecarregado e sufocado com muito de uma só vez. Alguns dos melhores traders de período integral hoje começaram como traders de período parcial primeiro.  </p>

<p><b>Existe uma taxa adicional para refazer o treinamento fornecido pela iFundTraders?</b><br />
Não. Como alunos da iFundTraders, você terá permissão para refazer toda a educação fornecida gratuitamente. Nunca haverá uma taxa adicional para a educação, o que torna nosso programa muito poderoso e sempre recompensador. Na verdade, a iFundTraders incentiva os traders a fazerem o programa de formação nada menos do que três vezes. Alguns têm feito até cinco vezes. Uma vez que você graduar completamente e integrar a nossa organização de trading como um trader profissional, você é uma parte da nossa família de traders. Sua educação e cuidado nunca vai acabar pois a iFundTraders continuará a orientá-lo durante o tempo que levar para fazer de você um trader mais bem-sucedido.</p>

<p><b>Qual é a política de cancelamento / reembolso para seus programas de treinamento?</b><br />
A venda é concluída e entregue assim que o pagamento é feito, como os Serviços do Programa são prestados de imediato na &quot;Central de Controle Online de Conta&quot; do membro, que inclui o acesso ao Manual de Treinamento com mais de 300 páginas com direitos de autor e de propriedade da iFT, que contém estratégias comprovadas, que poderia fornecer aos traders a capacidade de gerar renda em tempo inteiro, ao utilizar as estratégias fornecidas, juntamente com autodisciplina consistente. O acesso a Sala de Trading Online da IFT, onde discutimos, treinamos e educamos os traders diariamente em como extrair lucros dos mercados consistentemente, em um ambiente real de mercado ao vivo. É um tipo de ambiente de sala online. Acesso a sala de Suporte Online da IFT, onde nós fornecemos o suporte técnico a todos os traders usando as nossas plataformas proprietárias. Acesso ao download da plataforma e vídeos de treinamento para as plataformas que foram desenvolvidos pelos nossos melhores instrutores, especificamente para os nossos tarders. Os membros recebem acesso de imediato a estas coisas proprietárias que oferecemos mediante a pagamento; pedidos de reembolso ou cancelamento são inválidos uma vez que Serviços do Programa foram estabelecidos. Com isso dito, cada aluno da iFundTraders pode ganhar de volta todos os custos educacionais pagos através dos lucros de trading. Cada trader na Fase III recebe 100% de todos os lucros até que todos os custos educacionais foram recuperados. Somente após o reembolso total que a empresa e o trader começam a compartilhar a lucratividade do trader. </p>


<p><b>Posso efetuar o pagamento via cartão de crédito com iFundTraders?</b><br />
Sim, você pode pagar com cartão de crédito, mas todos os pagamentos de cartão de crédito não são reembolsáveis, e você deve responder a um e-mail de acordo, informando que você compreende esta política antes de processar o pagamento inicial.</p>



<p><b>Como posso optar por sair da nova taxa de assinatura mensal do programa de U$ 20?</b><br />
Qualquer trader que deseja sair da nova taxa de assinatura mensal do programa pode fazê-lo contatando <a href=mailto:accounting@ifundtraders.com>accounting@ifundtraders.com</a>. Serviços serão desativados e o status da conta atualizado em conformidade. Da mesma forma, qualquer trader que sente que foi faturada a nova taxa de assinatura mensal do programa por engano, por favor, contate <a href=mailto:accounting@ifundtraders.com>accounting@ifundtraders.com</a> e vamos processar o seu pedido de reembolso imediatamente, sem perguntas.</p>




