<p>
      <b> &iquest;Es iFundTraders LLC un br&oacute;ker?</b><br />
No, iFundTraders LLC es una firma de trading educativo con base en la ciudad de Nueva York que entrena y prepara individuos para unirse a las filas de los profesionales al convertirse en miembro de un equipo &eacute;lite de traders totalmente financiados y que comparten sus ganancias con la firma. Tras la plena realizaci&oacute;n de nuestro programa de entrenamiento, a cada trader se le ofrece la alternativa de convertirse en miembro de nuestra empresa de comercio preferente y operar una de sus cuentas.   </p>
<p>
<b>&iquest;iFundTraders me contratar&aacute;? </b><br />
No, no ser&aacute;s contratado en el sentido tradicional con salario y beneficios. Si completas nuestro paquete educativo de 7 d&iacute;as, se te ofrecer&aacute; la oportunidad de operar para nuestra empresa de trading preferente como trader profesional. Se te asignar&aacute; una cuenta totalmente financiada en nombre de esa empresa y tendr&aacute;s la oportunidad de ganarte la vida operando en los mercados. Recibir&aacute;s tu compensaci&oacute;n sobre la base de tu desempe&ntilde;o como trader profesional de la firma.   </p>
<p>
<b>&iquest;Cu&aacute;les ser&aacute;n mis responsabilidades como trader profesional una vez que me haya graduado del Programa Master Trader de iFundTraders? </b><br />
Tu &uacute;nica responsabilidad es operar. Los estudiantes de iFundTraders aprenden diversos m&eacute;todos de efectividad demostrada que han producido ganancias consistentes para muchos de nuestros traders existentes. En el transcurso de unos meses, el trader asentar&aacute; un estilo adaptado a su perfil y personalidad. Los estilos de trading nunca pueden ser gen&eacute;ricos, deben coincidir con su estructura psicol&oacute;gica. Es por esto que un trader puede hacer una fortuna con un m&eacute;todo, mientras otro trader puede perder una fortuna con el mismo m&eacute;todo. Nosotros te ayudaremos a encontrar el estilo de trading m&aacute;s adecuado para ti.  </p>

<p><b>&iquest;Tengo que depositar $5,000 como capital de riesgo o cualquier otra cantidad cuando est&eacute; listo para operar?</b><br />
&iexcl;No!, no es necesario un dep&oacute;sito de riesgo para los graduados de nuestros programas de entrenamiento que decidan convertirse en profesionales. Nuestros graduados comienzan sus carreras profesionales de trading con el capital de la empresa. Ellos no tienen que arriesgar su propio dinero. De hecho, la firma no obtiene ganancias hasta que t&uacute; no obtengas ganancias. En un sentido muy real, la empresa se convierte en tu socio s&oacute;lo para tus &eacute;xitos como trader. Si pierdes y renuncias, todas las p&eacute;rdidas las absorbe la empresa. Es s&oacute;lo cuando comienzas a ganar dinero que la empresa se beneficia. Esto nos da a nosotros y a nuestra firma de trading preferente todo el incentivo del mundo para convertirte en un trader rentable, tan r&aacute;pido como sea posible. </p>

<p><b>&iquest;Recibir&eacute; un salario si iFundTraders me contrata como trader?</b><br />
Cada estudiante de iFundTraders tiene la posibilidad de ser contratado como trader profesional por nuestra firma de trading preferente donde se les paga s&oacute;lo por su desempe&ntilde;o. Las compensaciones se basan en las ganancias y p&eacute;rdidas netas individuales de cada trader. Proporcionamos un Programa de Avance del Trader cuidadosamente estructurado que aumentar&aacute; el capital que operes a medida que demuestres una rentabilidad consistente. No hay l&iacute;mite en los niveles que se pueden conseguir, por lo que tu ingreso potencial es virtualmente ilimitado.  </p>

<p><b>Me gusta todo lo que he le&iacute;do &iquest;Es posible entrenarme y operar de forma remota con iFundTraders? </b><br />
&iexcl;S&iacute;! Hace algunos a&ntilde;os, la respuesta a esta pregunta habr&iacute;a sido un rotundo &ldquo;no&rdquo;. Hoy, gracias a los incre&iacute;bles avances tecnol&oacute;gicos, en el campo del trading profesional, podemos supervisar, asistir y entrenar a un trader remotamente, de una manera tan efectiva como la presencial. Muchos traders se desempe&ntilde;an mejor desde la comodidad y soledad de sus hogares. Es m&aacute;s, nuestros alumnos y traders a distancia experimentan todo lo que sucede en un piso principal de trading a trav&eacute;s de nuestra sala privada virtual de operaciones basada en ambiente web.  </p>

<p><b>&iquest;C&oacute;mo comienzo a ser estudiante y trader? </b><br />
Para graduarte en iFundTraders debes completar exitosamente las siguientes dos fases de entrenamiento: </p>

<p><strong><i>Fase I:</i></strong> Programa Master Trader &ndash; Estos son 7 d&iacute;as completos de entrenamiento intenso. Este es el programa que ha generado la mayor cantidad de traders exitosos para iFundTraders. <br />
<strong><i>Fase II:</i></strong> Modo de Operaci&oacute;n Simulada &ndash; Esta es la fase en que a cada trader se le entrega una cuenta de trading simulada con datos de mercado reales y en vivo con la que podr&aacute; perfeccionar las t&aacute;cticas de trading y las sofisticadas estrategias aprendidas en el Programa Master Trader. Al avanzar con &eacute;xito al Nivel 4 de nuestra Tabla de Avance, el trader ser&aacute; candidato a convertirse en un verdadero trader profesional con una cuenta financiada de nuestra empresa de trading preferente. A esto le llamamos Fase III: Trading Profesional. El 100% de todas las ganancias producidas ir&aacute;n al bolsillo del trader hasta que haya recuperado toda lo que ha invertido en su programa educativo en iFundTraders. Esto en efecto hace que el programa sea gratuito para quienes progresan. </p>
  <p>
<b>Una vez que haya logrado ser profesional en la Fase III, &iquest;Cu&aacute;ndo podr&eacute; retirar una parte o todas mis ganancias? </b><br />
Mensualmente se emiten cheques de pago correspondientes al mes anterior en que fueron generadas las ganancias. Por ejemplo, las ganancias producidas durante el mes de enero se pagar&aacute;n a m&aacute;s tardar el 20 de febrero.
</p>
<p>
<b>&iquest;Puedo solicitar m&aacute;s poder de compra como trader profesional en la Fase III?</b><br />
S&iacute;. Sin embargo este aumento siempre se otorgar&aacute; basado en el desempe&ntilde;o de tu operativa. En nuestro programa estructurado, el trader profesional en Fase III ganar&aacute; cada vez m&aacute;s exposici&oacute;n a un capital mayor. Como sabe la mayor&iacute;a de los traders, a mayor poder de compra, mayor es el potencial de ganancia. El potencial extra se gana m&aacute;s que nada con &ldquo;consistencia&rdquo;. En el mundo del trading profesional, la consistencia es la que marca pauta, la consistencia junto con bajas p&eacute;rdidas y/o peque&ntilde;as oscilaciones de la cuenta. Algunos traders han desarrollado una gran consistencia con muy pocos d&iacute;as negativos cada mes y con oscilaciones casi inexistentes.  </p>

<p><b>Me encanta el programa que ofrece iFundTraders, pero no puedo operar a tiempo completo por ahora. &iquest;Puedo ser un estudiante de iFundTraders a tiempo parcial?</b><br />

S&iacute;. Los traders de tiempo parcial frecuentemente se convierten en nuestros mejores traders. En primer lugar, su crecimiento y desarrollo como traders profesionales sucede en periodos m&aacute;s cortos y digeribles. En segundo lugar, las lecciones aprendidas son estables y la naturaleza parcial de su operativa reduce el riesgo de sobrecargarse y abrumarse con demasiadas cosas a la vez. Algunos de los mejores traders de tiempo completo de hoy comenzaron primero como traders de tiempo parcial.  </p>

<p><b>&iquest;Hay un pago adicional para repetir el entrenamiento proporcionado por iFundTraders?</b><br />
No. Como estudiante de iFundTraders, se te permite repetir toda la educaci&oacute;n proporcionado sin pagos extra. Nunca habr&aacute; un cargo adicional, lo cual hace que nuestros programas sean muy poderosos y eternamente valiosos. De hecho, iFundTraders alienta a los estudiantes a ver el programa completo de entrenamiento, no menos de tres veces. Algunos lo han visto hasta 5 veces. Una vez que te has graduado y te incorporas a nuestra organizaci&oacute;n como trader profesional, ser&aacute;s parte de nuestra familia de traders. Tu educaci&oacute;n y crecimiento nunca terminan porque iFundTraders seguir&aacute; gui&aacute;ndote todo el tiempo que sea necesario para convertirte en un trader m&aacute;s exitoso.</p>

<p><b>&iquest;C&oacute;mo es la pol&iacute;tica de devoluci&oacute;n/cancelaci&oacute;n de sus programas de entrenamiento?</b><br />
La venta se completa y se entrega una vez recibido el pago. Los Servicios del Programa se entregan inmediatamente en el &ldquo;Centro de Control de Cuenta En L&iacute;nea&rdquo; con acceso a todo el material registrado de propiedad intelectual que incluye el Manual de Entrenamiento iFT de m&aacute;s de 300 p&aacute;ginas que contiene estrategias de efectividad demostrada que pueden proporcionar a los traders ingresos de tiempo completo cuando se utilizan junto con una buena disciplina. Acceso a la Sala de Trading Online de iFT donde discutimos, entrenamos y educados traders diariamente sobre c&oacute;mo sacar ganancias consistentemente de los mercados en un entorno de mercado real y en vivo. Acceso a la Sala de Entrenamiento de iFT donde discutimos, entrenamos y educados traders diariamente sobre c&oacute;mo sacar ganancias consistentemente de los mercados en un entorno de sala de clases online. Acceso a la Sala de Soporte Online de iFT donde proporcionamos soporte t&eacute;cnico a todos los traders utilizando nuestras plataformas propietarias. Acceso a la plataforma de Descargas y videos de entrenamiento para las plataformas desarrollados por nuestros instructores estrella espec&iacute;ficamente para nuestros traders. Como los miembros reciben acceso inmediato a todos esos productos y servicio propietarios una vez completado el pago; la devoluci&oacute;n o cancelaci&oacute;n resulta inv&aacute;lida una vez que los Servicios del Programa han sido entregados. Dicho eso, cada estudiante de iFundTraders (bien sea el programa Master Trader de Forex o de Acciones) puede recuperar todos los pagos realizados a trav&eacute;s de sus ganancias operativas. Todos los traders en la Fase III obtienen el 100% de todas sus ganancias hasta haber recuperado la totalidad de lo invertido en su educaci&oacute;n. S&oacute;lo despu&eacute;s del reembolso completo es que la empresa comenzar&aacute; a compartir la rentabilidad del trader. </p>


<p><b>&iquest;Puedo pagar a iFundTraders utilizando mi tarjeta de cr&eacute;dito?</b><br />
S&iacute;, puedes pagar con tu tarjeta de cr&eacute;dito pero ning&uacute;n pago con tarjeta de cr&eacute;dito es reembolsable, por lo tanto debes responder a un acuerdo v&iacute;a correo electr&oacute;nico manifestando que entiendes y aceptas esta condici&oacute;n antes de que podamos procesar tu pago inicial.</p>



<p><b>&iquest;C&oacute;mo puedo suspender el nuevo pago mensual de $20 de suscripci&oacute;n del programa?</b><br />
Cualquier trader que desee suspender el nuevo pago mensual de suscripci&oacute;n del programa debe hacerlo contactando a <a href="mailto:accounting@ifundtraders.com">accounting@ifundtraders.com</a>. Los servicios se desactivar&aacute;n y el estatus de la cuenta se actualizar&aacute; en consecuencia. De la misma forma, cualquier trader que crea que se le ha sido cobrada la mensualidad por error debe contactar a <a href="mailto:accounting@ifundtraders.com">accounting@ifundtraders.com</a> y procesaremos el reembolso inmediatamente sin preguntas.</p>




